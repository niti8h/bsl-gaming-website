    <script>
    document.addEventListener("DOMContentLoaded", function () {
        const items = document.querySelectorAll(".wagergeeks-job-item");
        const searchInput = document.getElementById("searchInput");
        const categoryFilter = document.getElementById("categoryFilter");
        const locationFilter = document.getElementById("locationFilter");
        const jobDetails = document.getElementById("jobDetails");
        const jobList = document.getElementById("jobList");
        
        // Mobile popup elements
        const mobilePopup = document.getElementById("mobileJobPopup");
        const mobilePopupContent = document.getElementById("mobileJobPopupContent");
        const closePopup = document.querySelector(".close-popup");

        function activateItem(item) {
            items.forEach(i => i.classList.remove("active"));
            item.classList.add("active");

            const jobId = item.getAttribute("data-id");
            fetch('../wp-admin/admin-ajaxf9d6.html?action=load_job_details&amp;job_id=' + jobId)
                .then(res => res.text())
                .then(data => {
                    // Check if mobile view
                    if (window.innerWidth <= 768) {
                        // Show mobile popup
                        mobilePopupContent.innerHTML = data;
                        mobilePopup.style.display = "block";
                        document.body.style.overflow = "hidden";
                        
                        // Scroll to top of popup content
                        mobilePopup.scrollTo(0, 0);
                    } else {
                        // Show in regular details section
                        jobDetails.innerHTML = data;
                    }
                });
        }

        // Close popup handler
        closePopup.addEventListener("click", function() {
            mobilePopup.style.display = "none";
            document.body.style.overflow = "auto";
        });

        // Close popup when clicking outside content
        mobilePopup.addEventListener("click", function(e) {
            if (e.target === mobilePopup) {
                mobilePopup.style.display = "none";
                document.body.style.overflow = "auto";
            }
        });

        items.forEach(item => {
            item.addEventListener("click", () => activateItem(item));
        });

        function filterJobs() {
            const query = searchInput.value.toLowerCase();
            const selectedCat = categoryFilter.value.toLowerCase();
            const selectedLoc = locationFilter.value.toLowerCase();

            let matchFound = false;
            let firstVisibleItem = null;

            items.forEach(item => {
                const title = item.getAttribute("data-title").toLowerCase();
                const category = item.getAttribute("data-category").toLowerCase();
                const location = item.getAttribute("data-location").toLowerCase();

                const matchesSearch =
                    !query ||
                    title.includes(query) ||
                    location.includes(query) ||
                    category.includes(query);

                const matchesCat =
                    !selectedCat || category === selectedCat;

                const matchesLoc =
                    !selectedLoc || location === selectedLoc;

                const matches = matchesSearch && matchesCat && matchesLoc;

                if (matches) {
                    item.style.display = "block";
                    if (!matchFound && !firstVisibleItem) {
                        firstVisibleItem = item;
                    }
                    matchFound = true;
                } else {
                    item.style.display = "none";
                }
            });

            if (firstVisibleItem) {
                activateItem(firstVisibleItem);
                
                // On mobile, scroll to the first matching item
                if (window.innerWidth <= 768) {
                    firstVisibleItem.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
                }
            }

            if (!matchFound) {
                jobDetails.innerHTML = "<p style='padding:20px;'>No matching jobs found.</p>";
                if (window.innerWidth <= 768) {
                    mobilePopupContent.innerHTML = "<p style='padding:20px;'>No matching jobs found.</p>";
                }
            }
        }

        [searchInput, categoryFilter, locationFilter].forEach(el => {
            el.addEventListener("input", filterJobs);
            el.addEventListener("change", filterJobs);
        });
        
        // Handle window resize
        window.addEventListener("resize", function() {
            if (window.innerWidth > 768 && mobilePopup.style.display === "block") {
                mobilePopup.style.display = "none";
                document.body.style.overflow = "auto";
            }
        });
    });
    </script>
